        public static SvgColourServer FromRgb(float r, float g, float b, float a = 1)
        {
            var color = System.Drawing.Color.FromArgb((int)System.Math.Round(a * 255f), (int)System.Math.Round(r * 255f), (int)System.Math.Round(g * 255f), (int)System.Math.Round(b * 255f));
            return new SvgColourServer(color);
        }

        public static SvgColourServer FromCMYK(float c, float m, float y, float k)
        {
            var r = (1 - c) * (1 - k);
            var g = (1 - m ) * (1 - k );
            var b = (1 - y ) * (1 - k );
            var color = System.Drawing.Color.FromArgb((int)System.Math.Round(r * 255f), (int)System.Math.Round(g * 255f), (int)System.Math.Round(b * 255f));
            return new SvgColourServer(color);
        }
